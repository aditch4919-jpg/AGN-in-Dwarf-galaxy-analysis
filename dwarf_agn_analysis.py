dwarf_bh["offset_dex"] = dwarf_bh["logMBH"] - dwarf_bh["predicted_logMBH"]

print(dwarf_bh[["name", "logMstar", "logMBH", "predicted_logMBH", "offset_dex"]].to_string(index=False))
print(f"\nMedian offset: {dwarf_bh['offset_dex'].median():.2f} dex")
